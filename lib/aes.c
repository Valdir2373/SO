static uint8_t xtime(uint8_t x) { return (uint8_t)((x<<1)^(x>>7 ? 0x1b : 0)); }
static uint8_t mul(uint8_t a, uint8_t b) {
    return (uint8_t)(
        ((b&1)?a:0) ^ ((b>>1&1)?xtime(a):0) ^
        ((b>>2&1)?xtime(xtime(a)):0) ^
        ((b>>3&1)?xtime(xtime(xtime(a))):0) ^
        ((b>>4&1)?xtime(xtime(xtime(xtime(a)))):0));
}

static uint8_t rcon[11] = {0x00,0x01,0x02,0x04,0x08,0x10,0x20,0x40,0x80,0x1b,0x36};

void aes128_init(aes128_ctx_t *ctx, const uint8_t key[16]) {
    uint32_t *rk = ctx->rk;
    int i;
    for (i=0;i<4;i++)
        rk[i]=((uint32_t)key[i*4]<<24)|((uint32_t)key[i*4+1]<<16)|
              ((uint32_t)key[i*4+2]<<8)|(uint32_t)key[i*4+3];
    for (i=4;i<44;i++) {
        uint32_t t = rk[i-1];
        if (i%4==0) {
            t = ((uint32_t)sbox[(t>>16)&0xff]<<24)|
                ((uint32_t)sbox[(t>>8)&0xff]<<16)|
                ((uint32_t)sbox[t&0xff]<<8)|
                ((uint32_t)sbox[(t>>24)&0xff]);
            t ^= ((uint32_t)rcon[i/4]<<24);
        }
        rk[i] = rk[i-4]^t;
    }
}

static void add_rk(uint8_t s[16], const uint32_t *rk) {
    int i;
    for (i=0;i<4;i++) {
        s[i*4]  ^=(uint8_t)(rk[i]>>24); s[i*4+1]^=(uint8_t)(rk[i]>>16);
        s[i*4+2]^=(uint8_t)(rk[i]>>8);  s[i*4+3]^=(uint8_t)(rk[i]);
    }
}

static void sub_bytes(uint8_t s[16]) { int i; for(i=0;i<16;i++) s[i]=sbox[s[i]]; }
static void rsub_bytes(uint8_t s[16]){ int i; for(i=0;i<16;i++) s[i]=rsbox[s[i]];}

static void shift_rows(uint8_t s[16]) {
    uint8_t t;
    t=s[1]; s[1]=s[5]; s[5]=s[9]; s[9]=s[13]; s[13]=t;
    t=s[2]; s[2]=s[10]; s[10]=t; t=s[6]; s[6]=s[14]; s[14]=t;
    t=s[3]; s[3]=s[15]; s[15]=s[11]; s[11]=s[7]; s[7]=t;
}
static void rshift_rows(uint8_t s[16]) {
    uint8_t t;
    t=s[13]; s[13]=s[9]; s[9]=s[5]; s[5]=s[1]; s[1]=t;
    t=s[2]; s[2]=s[10]; s[10]=t; t=s[6]; s[6]=s[14]; s[14]=t;
    t=s[7]; s[7]=s[11]; s[11]=s[15]; s[15]=s[3]; s[3]=t;
}

static void mix_cols(uint8_t s[16]) {
    int i;
    for (i=0;i<4;i++) {
        uint8_t *c=s+i*4, a=c[0],b=c[1],d=c[2],e=c[3];
        c[0]=mul(a,2)^mul(b,3)^d^e;
        c[1]=a^mul(b,2)^mul(d,3)^e;
        c[2]=a^b^mul(d,2)^mul(e,3);
        c[3]=mul(a,3)^b^d^mul(e,2);
    }
}
static void rmix_cols(uint8_t s[16]) {
    int i;
    for (i=0;i<4;i++) {
        uint8_t *c=s+i*4, a=c[0],b=c[1],d=c[2],e=c[3];
        c[0]=mul(a,0xe)^mul(b,0xb)^mul(d,0xd)^mul(e,0x9);
        c[1]=mul(a,0x9)^mul(b,0xe)^mul(d,0xb)^mul(e,0xd);
        c[2]=mul(a,0xd)^mul(b,0x9)^mul(d,0xe)^mul(e,0xb);
        c[3]=mul(a,0xb)^mul(b,0xd)^mul(d,0x9)^mul(e,0xe);
    }
}

void aes128_encrypt(const aes128_ctx_t *ctx, const uint8_t in[16], uint8_t out[16]) {
    uint8_t s[16]; int r;
    memcpy(s, in, 16);
    add_rk(s, ctx->rk);
    for (r=1;r<10;r++) {
        sub_bytes(s); shift_rows(s); mix_cols(s); add_rk(s, ctx->rk+r*4);
    }
    sub_bytes(s); shift_rows(s); add_rk(s, ctx->rk+40);
    memcpy(out, s, 16);
}

void aes128_decrypt(const aes128_ctx_t *ctx, const uint8_t in[16], uint8_t out[16]) {
    uint8_t s[16]; int r;
    memcpy(s, in, 16);
    add_rk(s, ctx->rk+40);
    for (r=9;r>=1;r--) {
        rshift_rows(s); rsub_bytes(s); add_rk(s, ctx->rk+r*4); rmix_cols(s);
    }
    rshift_rows(s); rsub_bytes(s); add_rk(s, ctx->rk);
    memcpy(out, s, 16);
}


static void ccm_ctr_block(const uint8_t nonce[13], uint32_t ctr, uint8_t blk[16]) {
    blk[0] = 0x01; 
    memcpy(blk+1, nonce, 13);
    blk[14]=(uint8_t)(ctr>>8);
    blk[15]=(uint8_t)(ctr);
}

static void xor16(uint8_t *a, const uint8_t *b) {
    int i; for(i=0;i<16;i++) a[i]^=b[i];
}

void aes_ccmp_encrypt(const uint8_t key[16], const uint8_t nonce[13],
                      const uint8_t *aad, uint16_t aad_len,
                      uint8_t *data, uint16_t data_len,
                      uint8_t mic_out[8]) {
    aes128_ctx_t ctx;
    aes128_init(&ctx, key);
    uint8_t b[16], x[16], y[16];
    int i;

    
    b[0] = 0x59; 
    memcpy(b+1, nonce, 13);
    b[14]=(uint8_t)(data_len>>8); b[15]=(uint8_t)(data_len);
    aes128_encrypt(&ctx, b, x);

    
    memset(b, 0, 16);
    b[0]=(uint8_t)(aad_len>>8); b[1]=(uint8_t)(aad_len);
    uint16_t copy = aad_len < 14 ? aad_len : 14;
    memcpy(b+2, aad, copy);
    xor16(x, b);
    aes128_encrypt(&ctx, x, x);
    if (aad_len > 14) {
        memset(b, 0, 16);
        memcpy(b, aad+14, aad_len-14 < 16 ? aad_len-14 : 16);
        xor16(x, b);
        aes128_encrypt(&ctx, x, x);
    }

    
    for (i=0; i<data_len; i+=16) {
        memset(b, 0, 16);
        uint16_t chunk = (uint16_t)(data_len-i); if(chunk>16)chunk=16;
        memcpy(b, data+i, chunk);
        xor16(x, b);
        aes128_encrypt(&ctx, x, x);
    }
    
    ccm_ctr_block(nonce, 0, b);
    aes128_encrypt(&ctx, b, y);
    for (i=0;i<8;i++) mic_out[i]=x[i]^y[i];

    
    uint32_t ctr = 1;
    for (i=0; i<data_len; i+=16) {
        ccm_ctr_block(nonce, ctr++, b);
        aes128_encrypt(&ctx, b, y);
        uint16_t chunk = (uint16_t)(data_len-i); if(chunk>16)chunk=16;
        uint16_t j; for(j=0;j<chunk;j++) data[i+j]^=y[j];
    }
}

int aes_ccmp_decrypt(const uint8_t key[16], const uint8_t nonce[13],
                     const uint8_t *aad, uint16_t aad_len,
                     uint8_t *data, uint16_t data_len,
                     const uint8_t mic_in[8]) {
    
    aes128_ctx_t ctx;
    aes128_init(&ctx, key);
    uint8_t b[16], y[16];
    int i;
    uint32_t ctr = 1;
    for (i=0; i<data_len; i+=16) {
        ccm_ctr_block(nonce, ctr++, b);
        aes128_encrypt(&ctx, b, y);
        uint16_t chunk=(uint16_t)(data_len-i); if(chunk>16)chunk=16;
        uint16_t j; for(j=0;j<chunk;j++) data[i+j]^=y[j];
    }
    uint8_t mic_check[8];
    aes_ccmp_encrypt(key, nonce, aad, aad_len, data, data_len, mic_check);
    
    ctr=1;
    for (i=0; i<data_len; i+=16) {
        ccm_ctr_block(nonce, ctr++, b);
        aes128_encrypt(&ctx, b, y);
        uint16_t chunk=(uint16_t)(data_len-i); if(chunk>16)chunk=16;
        uint16_t j; for(j=0;j<chunk;j++) data[i+j]^=y[j];
    }
    for (i=0;i<8;i++) if(mic_check[i]!=mic_in[i]) return -1;
    return 0;
}
