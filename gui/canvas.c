static uint32_t *cbuf   = 0;
static uint32_t  cw     = 0;
static uint32_t  ch     = 0;
static uint32_t  cpitch = 0;  

void canvas_init(uint32_t *buf, uint32_t width, uint32_t height, uint32_t pitch) {
    cbuf   = buf;
    cw     = width;
    ch     = height;
    cpitch = pitch / 4;
}

void canvas_putpixel(int x, int y, uint32_t color) {
    if (x < 0 || y < 0 || (uint32_t)x >= cw || (uint32_t)y >= ch) return;
    cbuf[y * cpitch + x] = color;
}

void canvas_fill_rect(int x, int y, int w, int h, uint32_t color) {
    int px, py;
    for (py = y; py < y + h; py++) {
        if (py < 0 || (uint32_t)py >= ch) continue;
        for (px = x; px < x + w; px++) {
            if (px < 0 || (uint32_t)px >= cw) continue;
            cbuf[py * cpitch + px] = color;
        }
    }
}

void canvas_draw_rect(int x, int y, int w, int h, uint32_t color) {
    canvas_fill_rect(x,     y,     w, 1, color);
    canvas_fill_rect(x,     y+h-1, w, 1, color);
    canvas_fill_rect(x,     y,     1, h, color);
    canvas_fill_rect(x+w-1, y,     1, h, color);
}

void canvas_draw_line(int x0, int y0, int x1, int y1, uint32_t color) {
    int dx = x1 - x0; if (dx < 0) dx = -dx;
    int dy = y1 - y0; if (dy < 0) dy = -dy;
    int sx = (x0 < x1) ? 1 : -1;
    int sy = (y0 < y1) ? 1 : -1;
    int err = dx - dy;

    while (1) {
        canvas_putpixel(x0, y0, color);
        if (x0 == x1 && y0 == y1) break;
        int e2 = 2 * err;
        if (e2 > -dy) { err -= dy; x0 += sx; }
        if (e2 <  dx) { err += dx; y0 += sy; }
    }
}

void canvas_draw_circle(int cx, int cy, int r, uint32_t color) {
    int x = r, y = 0, err = 1 - r;
    while (x >= y) {
        canvas_putpixel(cx+x, cy+y, color); canvas_putpixel(cx-x, cy+y, color);
        canvas_putpixel(cx+x, cy-y, color); canvas_putpixel(cx-x, cy-y, color);
        canvas_putpixel(cx+y, cy+x, color); canvas_putpixel(cx-y, cy+x, color);
        canvas_putpixel(cx+y, cy-x, color); canvas_putpixel(cx-y, cy-x, color);
        y++;
        if (err < 0) { err += 2*y+1; }
        else         { x--; err += 2*(y-x)+1; }
    }
}

void canvas_fill_circle(int cx, int cy, int r, uint32_t color) {
    int x, y;
    for (y = -r; y <= r; y++) {
        for (x = -r; x <= r; x++) {
            if (x*x + y*y <= r*r)
                canvas_putpixel(cx+x, cy+y, color);
        }
    }
}

void canvas_draw_rounded_rect(int x, int y, int w, int h, int r, uint32_t color) {
    canvas_draw_line(x+r,   y,     x+w-r-1, y,     color);  
    canvas_draw_line(x+r,   y+h-1, x+w-r-1, y+h-1, color);  
    canvas_draw_line(x,     y+r,   x,       y+h-r-1, color); 
    canvas_draw_line(x+w-1, y+r,   x+w-1,   y+h-r-1, color); 
    
    int xi, yi, err = 1 - r;
    xi = r; yi = 0;
    while (xi >= yi) {
        canvas_putpixel(x+w-1-r+xi, y+r-yi,   color);
        canvas_putpixel(x+r-xi,     y+r-yi,   color);
        canvas_putpixel(x+w-1-r+xi, y+h-1-r+yi, color);
        canvas_putpixel(x+r-xi,     y+h-1-r+yi, color);
        canvas_putpixel(x+w-1-r+yi, y+r-xi,   color);
        canvas_putpixel(x+r-yi,     y+r-xi,   color);
        canvas_putpixel(x+w-1-r+yi, y+h-1-r+xi, color);
        canvas_putpixel(x+r-yi,     y+h-1-r+xi, color);
        yi++;
        if (err < 0) err += 2*yi+1;
        else         { xi--; err += 2*(yi-xi)+1; }
    }
}

void canvas_fill_rounded_rect(int x, int y, int w, int h, int r, uint32_t color) {
    canvas_fill_rect(x+r, y,   w-2*r, h,   color);
    canvas_fill_rect(x,   y+r, r,     h-2*r, color);
    canvas_fill_rect(x+w-r, y+r, r,   h-2*r, color);
    canvas_fill_circle(x+r,   y+r,   r, color);
    canvas_fill_circle(x+w-r-1, y+r,   r, color);
    canvas_fill_circle(x+r,   y+h-r-1, r, color);
    canvas_fill_circle(x+w-r-1, y+h-r-1, r, color);
}


static uint32_t lerp_color(uint32_t c1, uint32_t c2, int t, int max) {
    uint8_t r1 = (c1 >> 16) & 0xFF, g1 = (c1 >> 8) & 0xFF, b1 = c1 & 0xFF;
    uint8_t r2 = (c2 >> 16) & 0xFF, g2 = (c2 >> 8) & 0xFF, b2 = c2 & 0xFF;
    uint8_t r = r1 + (r2 - r1) * t / max;
    uint8_t g = g1 + (g2 - g1) * t / max;
    uint8_t b = b1 + (b2 - b1) * t / max;
    return ((uint32_t)r << 16) | ((uint32_t)g << 8) | b;
}

void canvas_fill_gradient(int x, int y, int w, int h,
                           uint32_t top, uint32_t bot) {
    int py;
    for (py = 0; py < h; py++) {
        uint32_t color = lerp_color(top, bot, py, h);
        canvas_fill_rect(x, y+py, w, 1, color);
    }
}


